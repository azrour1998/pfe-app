Avant de commencer à utiliser l'application, vous devrez effectuer une configuration initiale en créant un compte d'utilisateur et en ajoutant les informations sur votre entreprise, y compris le nom de l'entreprise, l'adresse, les numéros de téléphone et les informations fiscales. Vous pouvez également personnaliser les préférences de l'application pour répondre aux besoins de votre entreprise.

